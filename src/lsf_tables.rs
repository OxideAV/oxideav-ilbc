/// `split` selects which of the three splits to read (0, 1, 2).
/// `index` selects the row within that split (0..SPLIT_SIZES\[split\]).
/// Returns a vector of `SPLIT_DIMS[split]` floats representing the
/// corresponding sub-range of the LSF vector.
pub fn lookup_split(split: usize, index: u16) -> [f32; 4] {
    debug_assert!(split < 3);
    let size = SPLIT_SIZES[split];
    let idx = (index as usize) % size;
    let mut out = [0.0f32; 4];
    match split {
        0 => {
            let row = &LSF_CB_TBL_1[idx];
            out[0] = row[0];
            out[1] = row[1];
            out[2] = row[2];
        }
        1 => {
            let row = &LSF_CB_TBL_2[idx];
            out[0] = row[0];
            out[1] = row[1];
            out[2] = row[2];
        }
        2 => {
            let row = &LSF_CB_TBL_3[idx];
            out[0] = row[0];
            out[1] = row[1];
            out[2] = row[2];
            out[3] = row[3];
        }
        _ => unreachable!(),
    }
    out
}

/// Convenience: assemble the 10-dim LSF vector from three split
/// indices.
pub fn assemble_lsf(indices: &[u16; 3]) -> [f32; LPC_ORDER] {
    let mut lsf = [0.0f32; LPC_ORDER];
    for s in 0..3 {
        let dim = SPLIT_DIMS[s];
        let off = SPLIT_OFFSETS[s];
        let sub = lookup_split(s, indices[s]);
        for j in 0..dim {
            lsf[off + j] = sub[j];
        }
    }
    lsf
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn mean_lsf_is_monotone() {
        for k in 1..LPC_ORDER {
            assert!(
                LSF_MEAN[k] > LSF_MEAN[k - 1],
                "LSF_MEAN not increasing at {k}: {} -> {}",
                LSF_MEAN[k - 1],
                LSF_MEAN[k]
            );
        }
    }

    #[test]
    fn split_dims_sum_to_lpc_order() {
        let s: usize = SPLIT_DIMS.iter().sum();
        assert_eq!(s, LPC_ORDER);
    }

    #[test]
    fn split_sizes_correct() {
        assert_eq!(LSF_CB_TBL_1.len(), 64);
        assert_eq!(LSF_CB_TBL_2.len(), 128);
        assert_eq!(LSF_CB_TBL_3.len(), 128);
    }

    #[test]
    fn lookup_split_stays_in_range() {
        for split in 0..3 {
            for i in 0..SPLIT_SIZES[split] as u16 {
                let v = lookup_split(split, i);
                for j in 0..SPLIT_DIMS[split] {
                    assert!(v[j].is_finite());
                    assert!(v[j] > 0.0);
                }
            }
        }
    }

    #[test]
    fn rfc_first_row_bit_exact() {
        // First row of split 1 from Appendix A.8:
        // (float)0.155396, (float)0.273193, (float)0.451172
        let v = lookup_split(0, 0);
        assert_eq!(v[0], 0.155396);
        assert_eq!(v[1], 0.273193);
        assert_eq!(v[2], 0.451172);

        // First row of split 2:
        let v = lookup_split(1, 0);
        assert_eq!(v[0], 1.331177);
        assert_eq!(v[1], 1.576782);
        assert_eq!(v[2], 1.779541);

        // First row of split 3:
        let v = lookup_split(2, 0);
        assert_eq!(v[0], 1.705688);
        assert_eq!(v[1], 2.153809);
        assert_eq!(v[2], 2.398315);
        assert_eq!(v[3], 2.743408);

        // Last row of split 3:
        let v = lookup_split(2, 127);
        assert_eq!(v[0], 1.938232);
        assert_eq!(v[1], 2.264404);
        assert_eq!(v[2], 2.529053);
        assert_eq!(v[3], 2.796143);
    }
}
